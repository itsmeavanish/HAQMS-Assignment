const path = require('path');
const { URL } = require('url');
const dotenv = require('dotenv');
const { PrismaClient } = require('@prisma/client');
const { Client } = require('pg');

dotenv.config({ path: path.resolve(process.cwd(), '.env') });

const targetDatabaseUrl = process.env.TARGET_DATABASE_URL || process.env.SUPABASE_DATABASE_URL;

if (!targetDatabaseUrl) {
  throw new Error('Missing TARGET_DATABASE_URL or SUPABASE_DATABASE_URL.');
}

const targetUrl = new URL(targetDatabaseUrl);
const remoteSsl = targetUrl.hostname.endsWith('.supabase.co') ? { rejectUnauthorized: false } : undefined;

const prisma = new PrismaClient();

const createSchemaSql = [
  `CREATE TABLE IF NOT EXISTS "User" (
    "id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "email" TEXT NOT NULL UNIQUE,
    "password" TEXT NOT NULL,
    "name" TEXT NOT NULL,
    "role" TEXT NOT NULL DEFAULT 'RECEPTIONIST',
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP
  )`,
  `CREATE TABLE IF NOT EXISTS "Doctor" (
    "id" TEXT PRIMARY KEY,
    "userId" INTEGER NOT NULL UNIQUE,
    "name" TEXT NOT NULL,
    "specialization" TEXT NOT NULL,
    "department" TEXT NOT NULL,
    "experience" INTEGER NOT NULL,
    "consultationFee" INTEGER NOT NULL,
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "Doctor_userId_fkey" FOREIGN KEY ("userId") REFERENCES "User"("id") ON DELETE CASCADE
  )`,
  `CREATE TABLE IF NOT EXISTS "Patient" (
    "id" TEXT PRIMARY KEY,
    "name" TEXT NOT NULL,
    "email" TEXT UNIQUE,
    "phoneNumber" TEXT NOT NULL,
    "age" INTEGER NOT NULL,
    "gender" TEXT NOT NULL,
    "medicalHistory" TEXT,
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP
  )`,
  `CREATE TABLE IF NOT EXISTS "Appointment" (
    "id" TEXT PRIMARY KEY,
    "patientId" TEXT NOT NULL,
    "doctorId" TEXT NOT NULL,
    "appointmentDate" TIMESTAMP(3) NOT NULL,
    "reason" TEXT NOT NULL,
    "status" TEXT NOT NULL DEFAULT 'PENDING',
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "Appointment_patientId_fkey" FOREIGN KEY ("patientId") REFERENCES "Patient"("id") ON DELETE CASCADE,
    CONSTRAINT "Appointment_doctorId_fkey" FOREIGN KEY ("doctorId") REFERENCES "Doctor"("id") ON DELETE CASCADE,
    CONSTRAINT "Appointment_doctorId_appointmentDate_key" UNIQUE ("doctorId", "appointmentDate")
  )`,
  `CREATE TABLE IF NOT EXISTS "QueueToken" (
    "id" TEXT PRIMARY KEY,
    "tokenNumber" INTEGER NOT NULL,
    "patientId" TEXT NOT NULL,
    "doctorId" TEXT NOT NULL,
    "appointmentId" TEXT,
    "status" TEXT NOT NULL DEFAULT 'WAITING',
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "QueueToken_patientId_fkey" FOREIGN KEY ("patientId") REFERENCES "Patient"("id") ON DELETE CASCADE,
    CONSTRAINT "QueueToken_doctorId_fkey" FOREIGN KEY ("doctorId") REFERENCES "Doctor"("id") ON DELETE CASCADE,
    CONSTRAINT "QueueToken_appointmentId_fkey" FOREIGN KEY ("appointmentId") REFERENCES "Appointment"("id") ON DELETE CASCADE
  )`,
  `CREATE INDEX IF NOT EXISTS "Doctor_userId_idx" ON "Doctor" ("userId")`,
  `CREATE INDEX IF NOT EXISTS "Appointment_patientId_idx" ON "Appointment" ("patientId")`,
  `CREATE INDEX IF NOT EXISTS "Appointment_doctorId_idx" ON "Appointment" ("doctorId")`,
  `CREATE INDEX IF NOT EXISTS "Appointment_status_idx" ON "Appointment" ("status")`,
  `CREATE INDEX IF NOT EXISTS "QueueToken_patientId_idx" ON "QueueToken" ("patientId")`,
  `CREATE INDEX IF NOT EXISTS "QueueToken_doctorId_idx" ON "QueueToken" ("doctorId")`,
  `CREATE INDEX IF NOT EXISTS "QueueToken_appointmentId_idx" ON "QueueToken" ("appointmentId")`,
  `CREATE INDEX IF NOT EXISTS "QueueToken_status_idx" ON "QueueToken" ("status")`,
];

function toTimestamp(value) {
  return value ? new Date(value) : null;
}

async function main() {
  const localData = {
    users: await prisma.user.findMany({ orderBy: { id: 'asc' } }),
    doctors: await prisma.doctor.findMany({ orderBy: { id: 'asc' } }),
    patients: await prisma.patient.findMany({ orderBy: { id: 'asc' } }),
    appointments: await prisma.appointment.findMany({ orderBy: { id: 'asc' } }),
    queueTokens: await prisma.queueToken.findMany({ orderBy: { id: 'asc' } }),
  };

  const remote = new Client({ connectionString: targetDatabaseUrl, ssl: remoteSsl });
  await remote.connect();

  try {
    await remote.query('BEGIN');
    for (const statement of createSchemaSql) {
      await remote.query(statement);
    }

    await remote.query('TRUNCATE TABLE "QueueToken", "Appointment", "Doctor", "Patient", "User" RESTART IDENTITY CASCADE');

    for (const user of localData.users) {
      await remote.query(
        `INSERT INTO "User" ("id", "email", "password", "name", "role", "createdAt", "updatedAt")
         VALUES ($1, $2, $3, $4, $5, $6, $7)`,
        [user.id, user.email, user.password, user.name, user.role, toTimestamp(user.createdAt), toTimestamp(user.updatedAt)]
      );
    }

    for (const doctor of localData.doctors) {
      await remote.query(
        `INSERT INTO "Doctor" ("id", "userId", "name", "specialization", "department", "experience", "consultationFee", "createdAt", "updatedAt")
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9)`,
        [doctor.id, doctor.userId, doctor.name, doctor.specialization, doctor.department, doctor.experience, doctor.consultationFee, toTimestamp(doctor.createdAt), toTimestamp(doctor.updatedAt)]
      );
    }

    for (const patient of localData.patients) {
      await remote.query(
        `INSERT INTO "Patient" ("id", "name", "email", "phoneNumber", "age", "gender", "medicalHistory", "createdAt", "updatedAt")
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9)`,
        [patient.id, patient.name, patient.email, patient.phoneNumber, patient.age, patient.gender, patient.medicalHistory, toTimestamp(patient.createdAt), toTimestamp(patient.updatedAt)]
      );
    }

    for (const appointment of localData.appointments) {
      await remote.query(
        `INSERT INTO "Appointment" ("id", "patientId", "doctorId", "appointmentDate", "reason", "status", "createdAt", "updatedAt")
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8)`,
        [appointment.id, appointment.patientId, appointment.doctorId, toTimestamp(appointment.appointmentDate), appointment.reason, appointment.status, toTimestamp(appointment.createdAt), toTimestamp(appointment.updatedAt)]
      );
    }

    for (const queueToken of localData.queueTokens) {
      await remote.query(
        `INSERT INTO "QueueToken" ("id", "tokenNumber", "patientId", "doctorId", "appointmentId", "status", "createdAt", "updatedAt")
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8)`,
        [queueToken.id, queueToken.tokenNumber, queueToken.patientId, queueToken.doctorId, queueToken.appointmentId, queueToken.status, toTimestamp(queueToken.createdAt), toTimestamp(queueToken.updatedAt)]
      );
    }

    await remote.query('COMMIT');

    console.log('Transfer complete:', {
      users: localData.users.length,
      doctors: localData.doctors.length,
      patients: localData.patients.length,
      appointments: localData.appointments.length,
      queueTokens: localData.queueTokens.length,
    });
  } catch (error) {
    await remote.query('ROLLBACK');
    throw error;
  } finally {
    await remote.end();
    await prisma.$disconnect();
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});